//静的記憶域期間をもつオブジェクトの暗黙の初期化を確認

#include <stdio.h>

int fx; //０で初期化される

int main(void)
{
	static int si;	  //０で初期化される
	static double sd; //０.０で初期化される
	static int sa[5]; //全要素が０で初期化される

	printf("fx　＝　%d\n", fx);
	printf("si　＝　%d\n", si);
	printf("sd　＝　%f\n", sd);

	for (int i = 0; i < 5; i++)
	{
		printf("sa[%d]　＝　%d\n", i, sa[i]);
	}

	return 0;
}